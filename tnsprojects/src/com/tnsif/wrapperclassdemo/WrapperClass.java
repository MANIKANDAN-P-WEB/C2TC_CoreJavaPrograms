package com.tnsif.wrapperclassdemo;

public class WrapperClass {
	

		public static void main(String[] args) {
			
			
			
			//autoboxing -  
			//unboxing 
			
			int x = 9;
			//autoboxing
			Integer y = Integer.valueOf(x);
			
			//unboxing 
			
			int z = y;
			
			System.out.println(y +" "+ z);

		}

	}



